    /**
     * A {@link CameraCaptureSession.CaptureCallback} that handles events related to JPEG capture.
     */
    private CameraCaptureSession.CaptureCallback mCaptureCallback
            = new CameraCaptureSession.CaptureCallback() {

        private void process(CaptureResult result) {
            //long endTime = System.currentTimeMillis();
            Integer mode = result.get(CaptureResult.STATISTICS_FACE_DETECT_MODE);
            Face[] faces = result.get(CaptureResult.STATISTICS_FACES);

            //mode为0代表硬件支持人脸检测模式CaptureRequest.STATISTICS_FACE_DETECT_MODE_OFF，即不支持人脸检测
            if(faces == null || mode == null || mode == 0){
                Log.e(TAG, "detect error!");
                return;
            }
            //Log.e("tag", "face lengths: " + faces.length + " , detect mode : " + mode);
            if(faces.length == 0){
                interaction.process(preLocation);
                return;
            }

            //取最大的一张人脸
            Face maxFace = faces[0];
            for (int i = 1; i < faces.length; i++) {
                if (faces[i].getBounds().height() * faces[i].getBounds().width() > maxFace.getBounds().height() * maxFace.getBounds().width()) {
                    maxFace = faces[i];
                }
            }


            Point eyesCenter = new Point();
            //mode为1代表硬件仅支持人脸检测模式CaptureRequest.STATISTICS_FACE_DETECT_MODE_SIMPLE
            if(mode == 1){
                Rect faceArea = maxFace.getBounds();
                eyesCenter = new Point(faceArea.centerX(), faceArea.centerY());
                //Log.e("tag", "eyesCenter : " + eyesCenter.x + " " + eyesCenter.y);
            }
            //mode为2代表硬件仅支持人脸检测模式CaptureRequest.STATISTICS_FACE_DETECT_MODE_FULL
            else if(mode == 2){
                Point leftEye = maxFace.getLeftEyePosition();
                Point rightEye = maxFace.getRightEyePosition();
                eyesCenter = new Point((leftEye.x + rightEye.x) >> 1, (leftEye.y + rightEye.y) >> 1);
                //.e("tag", "leftEye: " + leftEye + " rightEye: " + rightEye + " eyesCenter: " + eyesCenter);
            }

            //10寸pad对应表
            int fusionMode = 0;
            if((eyesCenter.x > 670 && eyesCenter.x < 1200) || (eyesCenter.x > 1550 && eyesCenter.x < 2150)){
                fusionMode = 1;
            }

            //Log.e("tag", "loc: " + eyesCenter.x + " fusionMode: " + fusionMode);
            preLocation = eyesCenter.x;
            interaction.process(eyesCenter.x);

            //Log.e("tag", String.format("耗时 %d ms", endTime - startTime));
            //startTime = endTime;
        }

        @Override
        public void onCaptureProgressed(@NonNull CameraCaptureSession session,
                                        @NonNull CaptureRequest request,
                                        @NonNull CaptureResult partialResult) {
            process(partialResult);
        }

        @Override
        public void onCaptureCompleted(@NonNull CameraCaptureSession session,
                                       @NonNull CaptureRequest request,
                                       @NonNull TotalCaptureResult result) {
            process(result);
        }

    };

